 // Number
console.log("Number:", typeof 10);

// String
console.log("String:", typeof "10");

// Undefined
console.log("Undefined:", typeof undefined);

// Boolean
console.log("Boolean:", typeof false);

// Bigint
console.log("Bigint:", typeof 9007199254740991n);

// Null
console.log("null:", typeof null);

// Array
console.log("array:", typeof [10, 9, 8]);

 // Object
console.log("Object:", typeof { color: "red", size: "big" });

// Function
console.log("Function:", typeof function () {});
